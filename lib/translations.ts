// Dịch tự động các label ưu tiên từ file constants.ts
export const translatePriority = (vietnameseLabel: string, lang: string) => {
  // Nếu đang ở tiếng Việt thì giữ nguyên chữ gốc trong file constants
  if (lang === "vi") return vietnameseLabel; 
  
  // Nếu là tiếng Anh thì tra từ điển thu nhỏ này
  const englishDict: Record<string, string> = {
    "Khẩn cấp": "Urgent",
    "Ưu tiên cao": "High Priority",
    "Trung bình": "Medium",
    "Bình thường": "Normal",
    "Ưu tiên thấp": "Low Priority"
  };
  
  return englishDict[vietnameseLabel] || vietnameseLabel;
};
export const translateActivity = (content: string, lang: string) => {
  if (lang === "vi" || !content) return content;
  
  let translated = content;
  
  // Từ điển các hành động thường gặp
  const dict = [
    { vi: "đã chuyển công việc sang cột", en: "moved the task to column" },
    { vi: "đã tải lên tệp đính kèm", en: "uploaded an attachment" },
    { vi: "đã cập nhật người thực hiện", en: "updated assignees" },
    { vi: "đã cập nhật tên công việc", en: "updated the task title" },
    { vi: "đã cập nhật mô tả", en: "updated the description" },
    { vi: "đã cập nhật hạn chót", en: "updated the due date" },
    { vi: "đã cập nhật ngày bắt đầu", en: "updated the start date" },
    { vi: "đã cập nhật độ ưu tiên", en: "updated the priority" },
    { vi: "đã gửi một bình luận mới", en: "posted a new comment" },
    { vi: "đã thêm mục kiểm tra mới", en: "added a new checklist item" },
    { vi: "đã đánh dấu hoàn thành", en: "marked as complete" },
    { vi: "đã đánh dấu chưa hoàn thành", en: "marked as incomplete" },
    { vi: "đã cập nhật", en: "updated" }
  ];

  // Quét và thay thế chữ Tiếng Việt thành Tiếng Anh
  for (const item of dict) {
    if (translated.includes(item.vi)) {
      translated = translated.replace(item.vi, item.en);
    }
  }

  return translated;
};